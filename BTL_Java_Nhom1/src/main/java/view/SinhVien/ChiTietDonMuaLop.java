    public void setSinhVien(SinhVienLop sv) {
        txtMaSV.setText(sv.getMaSV());
        txtTenSV.setText(sv.getTenSV());
        String tenKhoa = sv.getTenKhoa();
        for (int i = 0; i < cboKhoa.getItemCount(); i++) {
            if (cboKhoa.getItemAt(i).equals(tenKhoa)) {
                cboKhoa.setSelectedIndex(i);
                break;
            }
        }
    }

