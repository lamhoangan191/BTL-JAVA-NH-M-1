    private void loadKhoaIntoComboBox() {
        List<String> dsTenKhoa = FakeData.layDSKhoa();
        cboKhoa.removeAllItems();
        for (String tenKhoa : dsTenKhoa) {
            cboKhoa.addItem(tenKhoa);
        }
    }
    
